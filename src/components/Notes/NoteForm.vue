<script setup>
import { onMounted, ref } from 'vue';
import { useNotesStore } from '../../stores/notes'
import { useToast } from 'vue-toastification'
import { object, string } from "yup"

const emit = defineEmits(['created'])

const noteFormSchema = object({
  content: string().required()
})

const noteData = ref({
  content: ''
})

const contentTextarea = ref(null)
const notesStore = useNotesStore()
const toast = useToast()

const onAddNote = () => {
  cleanNoteContent()

  try {
    noteFormSchema.validateSync(noteData.value);

    notesStore.addNote(noteData.value)
    toast.success('Nota criada com sucesso!')
    emit('created')
  } catch (error) {
    toast.info('Conteudo da nota nÃ£o pode ficar vazio!')
  }
}

const cleanNoteContent = () => {
  noteData.value.content = noteData.value.content.replace(/[\r\n]+/gm, "");
}

onMounted(() => {
  contentTextarea.value.focus()
})
</script>

<template>
  <form class="form">
    <div class="field">
      <textarea ref="contentTextarea" v-model="noteData.content" rows="6" v-on:keydown.enter="onAddNote"></textarea>
    </div>
  </form>
</template>

<style scoped>
.form {
  height: 100%;
}

.field textarea {
  font-size: 1.2em;
  resize: none;
  border: none;
  width: 100%;
}

.field textarea:focus {
  outline: 0;
}
</style>
